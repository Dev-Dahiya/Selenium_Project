public class WeightOnMoon {
	public static void main(String[] args){
		double weight_on_earth, weight_on_moon;
		
		weight_on_earth = 70;
		
		weight_on_moon = (weight_on_earth * 17) / 100;
		
		System.out.println("Weight on Earth: " + weight_on_earth);
		
		System.out.println("Weight on Moon: " + weight_on_moon);
		
	}

}
